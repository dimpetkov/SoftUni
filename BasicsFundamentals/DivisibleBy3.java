package BasicSyntaxCon_Lab;

public class P08DivisibleBy3 {
    public static void main(String[] args) {
            //numbers from 1 to 100 which are divisible by 3
            //check if is divisible by 3 -> print
            //starting point -> 1
            //increment: 1
            //end point -> 100
        for (int i =3; i <= 99; i+=3) {
                System.out.println(i);
        }
    }
}
